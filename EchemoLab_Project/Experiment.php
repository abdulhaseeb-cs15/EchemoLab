		<h3>The Theory</h3>

What are liquids?

Liquids are one of the three states of matter. They are able to flow and occupy the shape of the container.

What happens when a liquid is heated?

As a liquid is heated, its temperature increases. As its temperature increases, the molecules of the liquid gain energy and their kinetic energy increases. 
 As the kinetic energy increases, the molecular motion increases and the molecules of the liquid overcome the force of attraction between them.

On continuous heating, a particular temperature is reached where the molecules of the liquid leave the surface in the form of vapour. This produces a pressure above the liquid equal to the atmospheric pressure and the liquid starts boiling.
<br><br>At this stage, the temperature of the liquid remains stationary even on further heating. This stationary temperature at which the vapour pressure of the liquid is equal to the atmospheric pressure is called the boiling point of that liquid. At this temperature, bubbles begin to form and rise in the liquid. Before reaching this temperature,  
  The bubble is not forming because the atmospheric pressure is greater than the pressure in the bubbles and they collapse.<br>
  <br>Can you define the process of evaporation and boiling?<br><br>

Evaporation and boiling are two different processes. Evaporation is a process where liquid is converted into gas at any temperature
 below its melting point. Boiling is the process in which liquid is converted into vapour at its boiling point. Evaporation is a surface phenomenon, whereas boiling is a bulk phenomenon. Evaporation is the gradual vaporisation of a liquid on the surface Whereas the boiling is the rapid vaporisation of a liquid when it is heated to its boiling point. Below the boiling point,
 a liquid evaporates from its surface. At the boiling point, vapour bubbles come from the bulk of the liquid.
At an atmospheric pressure of exactly 760mm Hg (1 atm), the temperature at which a liquid boils is called the normal boiling point of the liquid. For water, the vapour pressure reaches the standard atmospheric pressure of 1 atmosphere at 100째C. So the normal boiling point of water is 100째C (212째F or 373K).The boiling point of pure water increases on the addition of soluble substances such 
as sugar or common salt. Boiling point of pure water increases with increase in pressure.<br>
<br>The quantity of heat required to completely vaporise a unit mass of a liquid gas at its boiling point is called latent heat of vaporisation of the liquid. It is represented by the symbol L. In the case of water the Latent heat of vaporisation is 22.57 x 105 J/kg at 100째C.</p>
<br><br>What are the factors that affect the boiling point of a liquid?<br><br>

<b>Pressure:</b> If the external pressure is higher than one atmosphere, the liquid will boil at a higher temperature than the normal boiling point. Example: In a pressure cooker, we increase the pressure so that the pressure inside the pressure cooker is greater than one atmosphere. So the water in the cooker boils at a higher temperature and food cooks more quickly. Conversely, if the external pressure is lower than one atmosphere, the liquid will boil at a lower temperature than the normal boiling point. Example: At higher elevations, such as hills and mountains, the atmospheric pressure is lower than one atmosphere, so water boils at a lower temperature than the normal boiling point.

<br><b>Molecule Types:</b> Types of molecules in the liquid affect the boiling point of the liquid. If the force of attraction between the molecules is relatively strong, the boiling point will be relatively high. If the force of attraction between molecules is relatively weak, the boiling point will be relatively low.
