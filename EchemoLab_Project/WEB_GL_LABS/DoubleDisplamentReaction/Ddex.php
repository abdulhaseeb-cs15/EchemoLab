      <p><h3>Our Objective</h3>
        To perform a double displacement reaction using sodium sulphate and barium chloride solutions.

<br>
		<h3>The Theory</h3>

Double displacement reactions may be defined as the chemical reactions in which one component each of both the reacting molecules is exchanged to form the products. During this reaction, the cations and anions of two different compounds switch places, forming two entirely different compounds.
 <br>The general equation which represents a double displacement reaction can be written as:
 <BR><br><center><img src="../images/db.png" width=500 height=150 ></center><br><br>

<b>Double displacement reactions generally take place in aqueous solutions in which the ions precipitate and there is an exchange of ions.</B><br>

For example, on mixing a solution of barium chloride with sodium sulphate, a white precipitate of barium sulphate is immediately formed. These reactions are ionic in nature. The reactants changes into ions when dissolved in water and there is an exchange of ions in solution. This results in the formation of product molecule.
   
<br><br>A double displacement reaction should also occur if an insoluble gas is formed.  Gases such as HCl and NH3  are soluble in water, but some other gases, such as H2S,  are not soluble in water.
Neutralization Reactions<br>
On mixing an aqueous solution of hydrochloric acid with an aqueous solution of sodium hydroxide, sodium chloride and water are formed.<br>
 <br><br><br>
 Precipitation Reactions<br>
On mixing aqueous solutions of silver nitrate and sodium chloride, a white curdy precipitate of silver chloride is formed.<br><br>
<br><br>On mixing an aqueous solution of barium chloride with that of copper sulphate, a white precipitate of barium sulphate is formed.
 
<br><br>On mixing an aqueous solution of lead nitrate with sodium sulphate, a white precipitate of lead sulphate is formed.
<br><br>On passing hydrogen sulphide gas through copper sulphate solution, a black precipitate of copper sulphide is formed.
<br><br>On adding a solution of lead nitrate to sodium iodide solution, a yellow precipitate of lead iodide is formed.
 
<br><br>Cobalt(II) chloride reacts with sodium carbonate to form pink/red coloured precipitate of cobalt(II) carbonate and sodium chloride.
 
<br><br>On adding aluminium sulphate solution to calcium chloride solution, a precipitate of calcium sulphate is formed.
 
<br><br>Lead acetate solution is treated with dilute hydrochloric acid to form lead chloride and acetic acid solution.<br><br>
 <h3>Learning Outcomes</h3>
<br>
1) Students understand terms such as double displacement reactions & different types of such reactions like neutralization, precipitation.<BR>
2) Students classify the compounds that give double displacement reactions.<BR>
3) Students acquire the skill to perform a double displacement reaction using barium chloride and sodium sulphate.<br>
4) Students will be able to distinguish a double displacement reaction from a given set of chemical reactions in future.<br>
