		<h3>The Theory</h3>

Let’s study how matter is classified!<br>

All matter can be classified into two categories: pure substances and mixtures.<br>

A pure substance consists of elements or compounds. Furthermore,mixtures can be classified into homogeneous (uniform appearance and composition throughout) and heterogeneous(made of different substances).<br>

<img src="../images/dcm.png" width=400 height=300 ><br><br>s


What are elements and compounds?<br><br>

Elements:<br><br>

 Elements are the simplest substances in nature. There are around 90 naturally occurring elements on earth. They cannot be broken down into anything simpler by physical or chemical means. They consist of only one kind of atom and they can exist as either atoms (e.g.,  argon, calcium, aluminium) or molecules (oxygen, nitrogen).<br><br>
 <img src="../images/dcm2.png" width=300 height=200 ><br><br>
<br>Compounds:<br><br>

<b>A compound is formed when two or more elements chemically combine together.</b>The elements react chemically and form chemical bonds between the atoms of the elements. In compounds the constituent elements are always present in fixed proportions and therefore a compound has a definite chemical composition.<br><br>

<b>The components of a compound lose their individual properties and the properties of a compound are different from their individual components.</b> For example, Iron is an element which is metallic in nature and has magnetic properties and oxygen is an element which is a colorless odorless gas. When iron rusts, 
it reacts with atmospheric oxygen to form the compound iron oxide.    <br><br>
<br><br>Iron oxide has different properties from those of iron and sulphur. Iron oxide is a reddish brown colored solid and is non-magnetic in nature.The components of a compound may be separated only by chemical means. Compounds are homogeneous in nature and have fixed melting points and boiling points.<br><br>
Now, what are mixtures and how they are classified?<br><br>

<b>Mixtures:</b><br><br>

A mixture is a combination of two or more substances which are not chemically the same. 

The composition of a mixture is variable.  In a mixture, the components are present in any proportion and they can be identified visually within the mixture. 
In a mixture, the individual components retain their own identities.
<br><br>
For example, when carbon disulphide is added to a mixture of iron and sulphur, sulphur first dissolves and then reappears in its solid state. First add a small amount of CS2 into the mixture of iron filings and sulphur taken in a test tube and shake gently. After shaking the iron filings settle to the bottom of the beaker and sulphur dissolves in CS2 to give a clear yellow solution.   Then, pour this clear yellow solution into a china dish and after some time the yellow coloured sulphur powder reappears in the china dish as CS2 evaporates.
