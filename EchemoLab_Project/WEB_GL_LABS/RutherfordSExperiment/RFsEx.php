      <p><h3>Our Objective</h3><br>
        
To demonstrate the scattering of alpha particles by gold foil.<br><br>
<b>Background:</b><br>
Model for the structure of an atom had been first proposed by J.J. Thomson. Later, followed many theories however, Rutherford's model was finally accepted as the correct nuclear model. Rutherford had shown his model with help of an experiment.
<br><br>Rutherford's scattering experiment:
 <b>Rutherford's model of an atom :</b><br>
 Ernest Rutherford was interested in knowing how the electrons are arranged within an atom. Rutherford designed an experiment for this. In this experiment, fast moving alpha (α)-particles were made to fall on a thin gold foil.
<br>•	He selected a gold foil because he wanted as thin a layer as possible. This gold foil was about 1000 atoms thick.
<br>•	α-particles are doubly-charged helium ions. Since they have a mass of 4µ, the fast-moving α-particles have a considerable amount of energy. 
<br><br>
It was expected that α-particles would be deflected by the sub-atomic particles in the gold atoms. Since the α-particles were much heavier than the protons, he did not expect to see large deflections. But, the α-particle scattering experiment gave totally unexpected results .
<br>Observations of Rutherford's scattering experiment:
<br><br>As we can see in Fig. 1.<br>
1.	Most of the fast moving α-particles passed straight through the gold foil.<br>
2.	Some of the α-particles were deflected by the foil by small angles.<br>
3.	Surprisingly one out of every 12,000 alpha particles appeared to rebound.<bR><br><br>
<br><center><img src="../images/Rf.png" width=500 height=300 ></center><br><br><br>
Conclusion of Rutherford's scattering experiment:<br>
1.	Most of the space inside the atom is empty because most of the α-particles passed through the gold foil without getting deflected.<br>
2.	Very few particles were deflected from their path, indicating that the positive charge of the atom occupies very little space.<br>
3.	A very small fraction of α-particles were deflected by very large angles, indicating that all the positive charge and mass of the gold atom were concentrated in a very small volume within the atom
<br><br>

From the data he also calculated that the radius of the nucleus is about 105 times less than the radius of the atom.<br>
Rutherford's Nuclear Model Of Atom<br>
On the basis of his experiment, Rutherford put forward the model of an atom, which had the following features:<br><br>
1.	There is a positively charged centre in an atom called the nucleus. Nearly all the mass of an atom resides in the nucleus.<br>
2.	The electrons revolve around the nucleus in well-defined orbits.<br>
3.	The size of the nucleus is very small as compared to the size of the atom.<br>
Rutherford's alpha particle scattering experiment shows the presence of nucleus in the atom.<br>
It also gives the following important information about the nucleus of an atom: <br><br>
1.	Nucleus of an atom is positively charged. <br>
2.	Nucleus of an atom is very dense and hard.<br>
3.	Nucleus of an atom is very small as compared to the size of the atom as a whole. <br>
Rutherford model of atom is also called Nuclear model of atom.



